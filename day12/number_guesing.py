LIFE_CONFIG = {'easy': 10,
                  'hard': 5}

print(BANNER)
print("Welcome to the game!")
level = input("Choose a difficulty. Type 'easy' or 'hard'.")
answer = np.random.randint(low = 1, high = 100)
life = LIFE_CONFIG[level]

while life > 0:
    print(f'You have {life} lives.')
    guess = int(input("Take a guess."))
    if guess == answer:
        break
    elif guess > answer:
        print('Too high.')
    else:
        print('Too low.')

    life -= 1

if life > 0:
    print('You are right!')
else:
    print(f'You have {life} lives, you lost.')